SELECT 'F (<60)', COUNT(*), ROUND(COUNT(*) / (SELECT COUNT(*) FROM scores WHERE is_deleted = 0) * 100, 2)
FROM scores WHERE is_deleted = 0 AND score < 60;

-- 各班级学生人数
SELECT c.class_name, COUNT(s.id) AS student_count
FROM students s
JOIN classes c ON s.class_id = c.id
WHERE s.is_deleted = 0
GROUP BY c.class_name;

-- 各学期成绩数量
SELECT sem.semester_name, COUNT(sc.id) AS score_count
FROM scores sc
JOIN semesters sem ON sc.semester_id = sem.id
WHERE sc.is_deleted = 0
GROUP BY sem.semester_name;
